<script setup>
import CoverUpload from "@/components/CoverUpload.vue";
import { ref, reactive, getCurrentInstance, nextTick } from "vue";
import { useRoute, useRouter } from "vue-router";
const { proxy } = getCurrentInstance();
const router = useRouter();
const route = useRoute();

const formData = ref({});
const formDataRef = ref();
const timeRangeref = ref();

const dialogConfig = reactive({
  show: false,
  title: "查看用户积分记录",
  buttons: [
    {
      type: "primary",
      text: "确定",
      click: (e) => {
        dialogConfig.show = false;
      },
    },
  ],
});

const showRecord = () => {
  dialogConfig.show = true;
  loadRecord();
  //   nextTick(() => {
  //     formDataRef.value.resetFields();
  //     loadRecord();
  //   });
};
defineExpose({ showRecord });

const loading = ref(false);
const recordInfo = ref({});
const loadRecord = async () => {
  loading.value = true;
  let params = {
    pageNo: recordInfo.value.pageNo,
  };

  if (formData.value.createTimeRange) {
    params.createTimeStart = formData.value.createTimeRange[0];
    params.createTimeEnd = formData.value.createTimeRange[1];
  }

  let result = await proxy.Request({
    url: proxy.globalInfo.api.loadUserIntegralRecord,
    params: params,
    showLoading: false,
  });
  loading.value = false;

  if (!result) {
    return;
  }
  recordInfo.value = result.data;
};

const closeHandler = () => {
  dialogConfig.show = false;
  formDataRef.value.resetFields();
};
</script>

<template>
  <Dialog
    :show="dialogConfig.show"
    :title="dialogConfig.title"
    :buttons="dialogConfig.buttons"
    width="500px"
    :showCancel="false"
    @close="closeHandler"
  >
    <el-form
      :model="formData"
      ref="formDataRef"
      label-width="40px"
      @submit.prevent
    >
      <!--input输入-->
      <el-form-item label="日期" prop="createTimeRange">
        <el-date-picker
          v-model="formData.createTimeRange"
          type="daterange"
          range-separator="~"
          start-placeholder="开始日期"
          ref="timeRangeref"
          end-placeholder="结束日期"
          value-format="YYYY-MM-DD"
          @change="loadRecord"
        ></el-date-picker>
      </el-form-item>
    </el-form>
    <div class="data-item">
      <div class="record-type">类型</div>
      <div class="integral">积分</div>
      <div class="create-time">时间</div>
    </div>
    <DataList
      :loading="loading"
      :dataSource="recordInfo"
      noDataMsg="暂无相关记录"
      @loadData="loadRecord"
    >
      <template #dataList="{ data }">
        <div class="data-item">
          <div class="record-type">{{ data.operTypeName }}</div>
          <div :class="['integral', data.integral > 0 ? 'add' : 'reduce']">
            {{ data.integral }}
          </div>
          <div class="create-time">{{ data.createTime }}</div>
        </div>
      </template>
    </DataList>
  </Dialog>
</template>

